//output worksheet key  

public class OutputWorkSheetKEY
{
	public static void main(String[] args)
	{
		System.out.println( "samben" );				// LINE 1
		
		System.out.println( "sam\tben" );			// LINE 2
		
		System.out.println( "sam\tben" );			// LINE 3
		
		System.out.println( "sam\\ben" );			// LINE 4
		
		System.out.println( 7 + 8 + 9 );				// LINE 5
		
		System.out.println( 3 + " " + 6 );			// LINE 6
		
		System.out.println("\" samben  \"");		// LINE 7
		
		System.out.println( "\\t\\sam ");				// LINE 8
		
		System.out.println( "\\\\ben" );					// LINE 9
		
		System.out.println( 5 + " " + 6 + 7);			// LINE 10

	}
}